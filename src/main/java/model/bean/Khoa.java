package model.bean;

public class Khoa {
	private String IdKhoa;
	private String TenKhoa;
	public String getIdKhoa() {
		return IdKhoa;
	}
	public void setIdKhoa(String idKhoa) {
		IdKhoa = idKhoa;
	}
	public String getTenKhoa() {
		return TenKhoa;
	}
	public void setTenKhoa(String tenKhoa) {
		TenKhoa = tenKhoa;
	}
}
